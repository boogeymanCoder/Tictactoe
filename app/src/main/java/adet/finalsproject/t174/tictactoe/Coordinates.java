package adet.finalsproject.t174.tictactoe;

public class Coordinates {
    private int row;
    private int col;

    Coordinates(int row, int col) {
        this.row = row;
        this.col = col;
    }

    public int getRow() {
        return row;
    }

    public int getCol() {
        return col;
    }
}
